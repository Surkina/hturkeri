#include <stdio.h>
#include <unistd.h>

unsigned int	ft_strlcpy(char *dest, const char *src, unsigned int size)
{
	unsigned int	i;

	i = 0;
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (i);
}

int main()
{
	char	a[] = "salih";
	char	b[] = "volkan";

	int	result = ft_strlcpy(a, b, 3);

	printf("%s", a);
}
