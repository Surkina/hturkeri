#include <unistd.h>

void	ft_putnbr(int ab)
{
	char	c;

	if (ab < 0)
	{
		write(1, "-", 1);
		ab = -ab;
	}
	if (ab >= 10)
		ft_putnbr(ab / 10);
	c = 48 + ab % 10;
	write(1, &c, 1);
}
